	<h3>Symbol</h3>
The symbol for the NSS has been based on the giant Rath Wheel of the world famous Konark Sun Temple (The Black Pagoda) situated in Orissa, India. The wheel portrays the cycle of creation, preservation and release and signifies the movement in life across time and space, The symbol thus stands for continuity as well as change and implies the continuous striving of NSS for social change.

	<h3>Badge</h3>
The NSS Symbol is embossed on the badge. The eight bars in the wheel represent the 24 hours of a day. The red colour indicates that the volunteer is full of young blood that is lively, active, energetic and full of high spirit. The navy blue colour indicates the cosmos of which the NSS is tiny part, ready to contribute its share for the welfare of the mankind.

	<h3>Administrative Structure at University Level</h3>
	<p class="text-justify"> The National Service Scheme covers students at the level of higher education. Thus the administrative structures of the higher education have the responsibility of looking after NSS and its implementation at college/school/universities level. The successful functioning of NSS cell at University level will give impetus for proper implementation of NSS in the unit level.</p>
	
