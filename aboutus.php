				<li>NSS being an academic extension programme the University will provide necessary infrastructure and other facilities like telephone, office and office equipment and secretarial assistance for the smooth functioning of the cell.</li>	
				<li>The cell will function under the Vice-Chancellor, the Programme Coordinator, NSS will be incharge of the cell and the main executive functionary.</li>
			</ol>
		</li>
