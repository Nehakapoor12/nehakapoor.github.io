			<ol type="i">
				<li>Term/Tenure of the Programme Coordinator<p class="text-justify">The Programme Coordinator will be appointed on deputation/short term contract for the period of three years extendable by one year further. No Programme Coordinator will be appointed on Permanent basis.</p></li>
				<li>Functions of the Programme Coordinators
