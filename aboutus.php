	<ol type="1">
		<li>NSS Unit<p class="text-justify">An institution will be allotted NSS units according to the strength of students. The number of units will be allotted by the Programme Coordinator  in consultation with NSS Regional Directorate and State NSS Officer considering the demands of the institution. It is expected that the institution will provide necessary facilities for the successful running of the NSS unit because it is a part of the institution i.e. college or school.</p>
			<ol type="i">
				<li>The strength of a unit will be 100 NSS volunteers normally. The strength of the NSS unit can be extended upto 120 volunteers in exceptional cases where second unit cannot be raised due to constraints. It is always preferable that a separate unit is started instead of enrolling more NSS volunteers.</li>
				<li>In exceptional cases where the total strength of students enrolled is very small, a smaller NSS unit can be started with the strength of 75 NSS volunteers.</li>
			</ol>
		</li>
		<li>Enrollment of NSS Volunteers<p class="text-justify">At college level the NSS volunteers will be enrolled from the first and second year degree-class students. Preference should be given to the students who have worked as NSS volunteers at +2 level also.</p>
			<ol type="i">
				<li>Students belonging to minority communities, scheduled castes and scheduled tribes should be encouraged to participate in NSS. They should be given due representation where more students desire to join NSS.</li>
				<li>In co-educational colleges the girls should be motivated to join NSS.</li>
				<li>The students from foreign countries studying in Indian universities should also be encouraged to join NSS so that they may share the experiences of national reconstruction and community work.</li>
				<li>NCC cadets will not be allowed to join NSS. Similarly NSS volunteers will not participate in NCC or any other youth organisation as long as they are in NSS. Same restriction will apply to the NSS Programme Officers also.</li>
