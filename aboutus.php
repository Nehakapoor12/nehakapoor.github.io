			<ol type="i">
				<li>Only those belonging to the teaching faculty will be considered for appointment as Programme Officer.</li>
				<li>Programme Officer will be responsible for the organisation of NSS unit, implementation of NSS programme under the supervision and direction of Principal of the college or head of the institution.</li>
				<li>The Programme Officer will be responsible to carry out the instructions issued by the Programme Coordinator  of the University, NSS Regional Directorate and State NSS Officer for the implementation of NSS activities as per the NSS Manual, programme guidelines and administrative and policy directives.</li>
				<li>There shall not be any clash between the instructions issued by the NSS Regional Directorate or State NSS Officer or Programme Coordinator  as these instructions are to be based on NSS Manual/Programme guidelines or administrative or policy directives issued by the Government of India.</li>
