	<h3>Motto</h3>
The Motto of NSS "Not Me But You", reflects the essence of democratic living and upholds the need for self-less service. NSS helps the students develop appreciation to other person's point of view and also show consideration to '/other living beings. The philosophy of the NSS is well doctrined in this motto, which underlines/on the belief that the welfare .of an individual is ultimately dependent on the welfare of the society on the whole and therefore, the NSS volunteers shall strive for the well-being of the society.<span>

