						<li>To ensure implementation of NSS Regular activities and special camping programmes.</li>
						<li>To ensure timely release of grants to colleges.</li>
						<li>To submit the reports and returns to Programme Adviser, Regional Centre, State Liaison Officer and ETIs.</li>
						<li> To ensure selection of new Programme Officers as per guidelines and ensure their orientation within the stipulated period.</li>
