	<h3>PROGRAMME OFFICER – APPOINTMENT, DUTIES AND FUNCTIONS</h3>
		<p class="text-justify">The Programme Officer is expected to motivate student youth to understand the values and philosophy of NSS. The overall functions of Programme Officer is to help the students to plan, implement, and evaluate the activities of NSS under his/her charge and give proper guidance and directions to the student volunteers.</p>
		
	<ol type="1">
		<li>To discharge his/her obligations under NSS Programme the Programme Officer plays the role of an organiser, an educator, a coordinator, a supervisor, an administrator, and public relation officer to improve the quality and magnitude of NSS programme in his/her institution. His/her functions can be stated as under :-
			<ol type="a">
				<li>As an Organizer
					<ol type="i">
						<li>To interpret the scheme to the students and other members of the college community and create awareness about the scheme;</li>
						<li>To Motivate, recruit and select student for NSS work;</li>
						<li>To enlist cooperation and coordination of community agencies, government departments and non-governmental agencies; and</li>
						<li>To select service projects on the basis of utility an feasibility.</li>
					</ol>
				</li>
				<li>As an Educator
					<ol type="i">
						<li>To prepare orientation programme for NSS volunteers, explain them about the concept of social service, and teach them methods and skills required for achieving the objectives of the scheme;</li>
						<li>To promote community education through meetings, talks, news bulletins discussions etc.; and</li>
						<li>To help in formulating NSS programmes which will have direct relationship with the academic curricula.</li>
					</ol>
				</li>
				<li>As a Coordinator
					<ol type="i">
						<li>To coordinate NSS activities in accordance with the students ability and community demands.</li>
						<li>To coordinate internal resources available in the form of teaching expertise of teachers for enhancing the knowledge and skills of the students in implementation of the scheme; and</li>
						<li>To coordinate various external resources available in the forms of government services; welfare agencies and voluntary bodies for the success of the NSS programme.</li>
					</ol>
				</li>
				<li>As a Supervisor
					<ol type="i">
						<li>To assist students to learn how to do their jobs. His/her supervisory and consultative skills should enable students to set realistic goals and see problems as challenge and take appropriate steps to solve them.</li>
						<li>To assist in evaluation and follow-up work.</li>
					</ol>
				</li>
				<li>As an Administrator
					<ol type="i">
						<li>To keep the Principal, College Advisory Committee  and the Programme Coordinator  of the University informed of the activities of the unit;</li>
						<li>To run day-to-day administration of the programme;</li>
						<li>To attend correspondence regularly;</li>
						<li>To maintain record of students participation and activities undertaken;</li>
						<li>To prepare progress report periodically for submission to college/school and university;</li>
						<li>To keep accounts and stock in the prescribed forms; and</li>
						<li>To prepare annual calendar of activities to be undertaken.</li>
					</ol>
				
				</li>
				<li>As a Public Relation Person 
					<ol type="i">
						<li>To inform the community about the scheme through press reports, radio and television programmes, pamphlets, seminars and speaker’s forums.</li>
						<li>To initiate IEC campaigns for image building of NSS in order to inspire and motivate the students and community.</li>
					</ol>
				
				</li>
			</ol>
		</li>
		<li>Selection of the Programme Officer<p class="text-justify">The selection of the Programme Officer will be made by the Principal of the institution in consultation with the Programme Coordinator  of the respective university/+2 level.</p>
			<ol type="i">
						<li>Qualification
							<ol type="a">
								<li>Programme Officer will be selected from the members of teaching faculty only.</li>
								<li>NCC Officers and Physical Education Directors should not be appointed as NSS Programme Officer.</li>
								<li>In women college/girls schools a lady teacher should be appointed as Programme Officer. However, male members may help the lady Programme Officer.</li>
								<li>A teacher who has high level of, motivation, inclination and aptitude for community work and above all very good report with students should be preferred as Programme Officer.</li>
							</ol>
						</li>
						<li>Tenure<p class="text-justify">The maximum period for which a teacher is appointed as Programme Officer will be 3 years in the first instance. However, this period is extendible upto 4th year, on the basis of the review of his/her performance by the Principal and Programme Coordinator.</p></li>
						<li>Training/Orientation<p class="text-justify">The Programme Officer will be sent for orientation course within 3 months of his/her selection. The Programme Officer must undergo the orientation training within one year of the date of his/her selection in case the orientation is not conducted in the stipulated period of 3 months.</p>
							<ol type="i">
								<li>The Principal of the institution will intimate to the Programme Coordinator, NSS Regional Directorate and ETIs concerned regarding the selection of the Programme Officer and necessary arrangement of the orientation of the selected Programme Officer. The Principal will also ensure that the Programme Officer is relieved for participation in orientation training organised by ETIs. Similarly, the Programme Officer is expected to attend refresher course after every two years and it is the obligation of the head of the institution to relieve the Programme Officer for this purpose.</li>
								<li>If the selected Programme Officer does not undergo the orientation training for any reason within one year from the date of his/her selection, he/she will cease to function as Programme Officers and another person will be selected and given training in time. No Programme Officer without orientation will continue to work as Programme Officer if he/she is not trained within the stipulated period.</li>
							</ol>
						
						</li>
						<li>Approach<p class="text-justify">The main objective of NSS programme is to prepare the NSS volunteers for the democratic, self-disciplined and self-reliant way of life. It is, therefore, of vital importance that the NSS units are organised and run on democratic lines. The student leaders, NSS leaders, NSS volunteers and other members of the staff, and eminent personalities from the community are also to be associated with it. They should be encouraged to participate in planning, execution and evaluation of NSS programme.</p></li>
			</ol>		
		</li>
	</ol>
